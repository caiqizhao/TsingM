package com.example.guaiwei.tsingm.bean;

public class RecommendFood {
    String foodName;//食物的名称
    String foodG;//食物克数
    String foodReLiang;//食物的热量
    public String getFoodName() {
        return foodName;
    }

    public void setFoodName(String foodName) {
        this.foodName = foodName;
    }

    public String getFoodG() {
        return foodG;
    }

    public void setFoodG(String foodG) {
        this.foodG = foodG;
    }

    public String getFoodReLiang() {
        return foodReLiang;
    }

    public void setFoodReLiang(String foodReLiang) {
        this.foodReLiang = foodReLiang;
    }
}
